    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sexo = "";

        if (rBtn1.isSelected()) {
            sexo = "Masculino";
        } else if (rBtn2.isSelected()) {
            sexo = "Femenido";
        }

        char[] arrayC = txtPassword.getPassword();
        String pass = new String(arrayC);

        f1.setApellido1(txtApellido1.getText().trim());
        f1.setApellido2(txtApellido2.getText().trim());
        f1.setCorreo(txtCorreo.getText().trim());
        f1.setFechaNacimiento(new Date(txtFechaNacimiento.getDate().getTime()));
        f1.setIdentificacion(txtIdentificacion.getText().trim());
        f1.setNombre1(txtNombre1.getText().trim());
        f1.setNombre2(txtNombre2.getText().trim());
        f1.setPassword(pass);
        f1.setSexo(sexo);
        f1.setTelefono(txtTelefono.getText().trim());
        f1.setRol(txtRol.getSelectedItem().toString());

        Jdbc cx = new Jdbc();
        FuncionarioJdbc fjdbc = new FuncionarioJdbc();
        try {
            cx.conectarme();
            fjdbc.setCon(cx.getCon());
            fjdbc.updateFuncionario(f1);

        } catch (SQLException ex) {
            Logger.getLogger(GUI_ModificarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        Jdbc cx = new Jdbc();
        FuncionarioJdbc fjdbc = new FuncionarioJdbc();
        try {
            cx.conectarme();
            fjdbc.setCon(cx.getCon());           
            
            f1 = fjdbc.getFuncionario(txtBuscarById.getText().trim());            
            txtApellido1.setText(f1.getApellido1());
            txtApellido2.setText(f1.getApellido2());
            txtCorreo.setText(f1.getCorreo());
            txtFechaNacimiento.setDate(new Date(f1.getFechaNacimiento().getTime()));
            txtIdentificacion.setText(f1.getIdentificacion());
            txtNombre1.setText(f1.getNombre1());
            txtNombre2.setText(f1.getNombre2());
            txtTelefono.setText(f1.getTelefono());
            txtRol.setSelectedItem(f1.getRol());
            txtPassword.setText(f1.getPassword());
            
            if ("Masculino".equals(f1.getSexo())) {
                rBtn1.setSelected(true);
            }else {
                rBtn2.setSelected(true);
            }
            

        } catch (SQLException ex) {
            Logger.getLogger(GUI_ModificarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button1ActionPerformed

